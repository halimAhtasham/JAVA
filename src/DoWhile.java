public class DoWhile {
    public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println(i+" Bangladesh");
            // i+=2;
            i++;
        } while (i<10);

        int y = 15;
        do {
            System.out.println("\n");
            System.out.println(y+" Bangladesh");
        } while (y < 10);

        //Do-While loop will run at least one time though the condition is false.
    }
}
